#include <unistd.h>

void	ft_print_combn(int n)
{
	int	nbr[n];
	int i;

	i = n;
	while (i > 0)
	{
		nbr[i - 1] = '0';
		write(1, &nbr[i - 1], 1);
		i--;
	}
}
